(function ($) {
    $(document).ready(function () {


        /* --------------------------------- .show() -------------------------------- */

        $('.btnShow').on('click', function () {
            $('.box').show(250);
        })

        /* -------------------------------------------------------------------------- */


        /* --------------------------------- .hide() -------------------------------- */

        $('.btnHide').on('click', function () {
            $('.box').hide();
        })

        /* -------------------------------------------------------------------------- */


        /* -------------------------------- .toggle() ------------------------------- */

        $('.btnToggle').on('click', function () {
            $('.box').toggle();
        })

        /* -------------------------------------------------------------------------- */


        /* ------------------------------ .slideDown() ------------------------------ */

        $('.btnSlideDown').on('click', function () {
            $('.box').slideDown();
        })

        /* -------------------------------------------------------------------------- */


        /* ------------------------------- .slideUp() ------------------------------- */

        $('.btnSlideUp').on('click', function () {
            $('.box').slideUp();
        })

        /* -------------------------------------------------------------------------- */


        /* ----------------------------- .slideToggle() ----------------------------- */

        $('.btnSlideToggle').on('click', function maFct() {
            $('.btnSlideToggle').off('click', maFct); // on bloque le bouton le temps de l'animation
            $('.box').slideToggle(2000, function () {
                $('.btnSlideToggle').on('click', maFct)
            });
        });

        /* -------------------------------------------------------------------------- */

        /* -------------------------------- .fadeIn() ------------------------------- */

        $('.btnFadeIn').on('click', function () {
            $('.box').fadeIn();
        })
        /* -------------------------------------------------------------------------- */


        /* ------------------------------- .fadeOut() ------------------------------- */

        $('.btnFadeOut').on('click', function () {
            $('.box').fadeOut();
        })

        /* ------------------------------- Style Arrow ------------------------------ */

        // $('.btnFadeOut').on('click', () => $('.box').fadeOut())

        /* -------------------------------------------------------------------------- */


        /* -------------------------------- .fadeTo() ------------------------------- */

        $('.btnFadeTo').on('click', function () {
            $('.box').fadeTo(2000, 0.5);
        }) // apparait jusqu'Ã  ...


        $('.btnFadeTo1').on('click', function () {
            $('.box').fadeTo(2000, 1);
        })

        /* -------------------------------------------------------------------------- */



    });

})(jQuery)